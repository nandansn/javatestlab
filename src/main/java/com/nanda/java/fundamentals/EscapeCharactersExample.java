public class EscapeCharactersExample {
    
    public static void main(String[] args) {
	
	System.out.println("Nanda\nkumar");
	System.out.println("Nanda\tkumar");
	System.out.println("nanda\rkumar");
	System.out.println("nanda\bkumar");
	System.out.println("name is \'nanda\'");
	System.out.println("name is \"nanda\"");
	System.out.println("name is \\nanda\\");
    }
    

}
